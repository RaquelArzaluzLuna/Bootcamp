/* 
 Operadores relacionales o de comparacion 
    >       Mayor que
    <       Menor que 
    >=      Mayor o igual
    <=      Menor o igual
    ==      Igual
    !=      Distinto
    ===     Operador estricto
    !===    Operador diferente estricto
*/

let num1 = 22;
let num2 = 15;

console.log('El primer numero es ' + num1);
console.log('El segundo numero es ' + num2);

console.log(num1 > num2);
console.log(num1 < num2);
console.log(num1 >= num2);
console.log(num1 <= num2);
console.log(num1 == num2);
console.log(num1 != num2);